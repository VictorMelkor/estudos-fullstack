// if executa um bloco se a condição for verdadeira

let idade = 17;

if (idade < 12) {
  console.log("Criança");
} else if (idade < 18) {
  console.log("Adolescente");
} else {
  console.log("Adulto");
}

// A ordem das condições importa: o primeiro que for verdadeiro será executado.
