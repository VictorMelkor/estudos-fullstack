// Objetos armazenam pares chave-valor
let pessoa = {
  nome: "Ana",
  idade: 30,
  profissao: "Engenheira"
};

// Acessar valores com ponto
console.log(pessoa.nome); // "Ana"
console.log(pessoa.profissao); // "Engenheira"

// Também é possível usar colchetes (menos comum)
console.log(pessoa["idade"]); // 30
