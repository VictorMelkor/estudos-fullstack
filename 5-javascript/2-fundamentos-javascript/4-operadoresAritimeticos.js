let a = 10;
let b = 3;

console.log(a + b); // 13 (adição)
console.log(a - b); // 7 (subtração)
console.log(a * b); // 30 (multiplicação)
console.log(a / b); // 3.333... (divisão)
console.log(a % b); // 1 (resto da divisão)

// Incremento e decremento
let c = 5;
c++;
console.log(c); // 6

c--;
console.log(c); // 5
