// JavaScript permite que o tipo de uma variável mude

let dado = 123;       // number
console.log(typeof dado); // "number"

dado = "agora é texto"; // string
console.log(typeof dado); // "string"

dado = true;         // boolean
console.log(typeof dado); // "boolean"

// Isso é poderoso, mas pode causar bugs se não for controlado
