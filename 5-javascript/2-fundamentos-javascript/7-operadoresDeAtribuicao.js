let numero = 10;

numero += 5; // número = número + 5 → 15
numero -= 3; // número = número - 3 → 12
numero *= 2; // número = número * 2 → 24
numero /= 4; // número = número / 4 → 6
numero %= 2; // número = número % 2 → 0

console.log(numero);
