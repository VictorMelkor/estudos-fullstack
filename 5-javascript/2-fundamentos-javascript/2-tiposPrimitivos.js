// ==========================
// TIPOS PRIMITIVOS
// ==========================

// String → texto
let nome = "Victor";

// Number → números inteiros ou decimais
let idade = 30;
let altura = 1.75;

// Boolean → verdadeiro ou falso
let ativo = true;
let admin = false;

// Null → ausência intencional de valor
let endereco = null;

// Undefined → variável declarada mas não inicializada
let telefone;
console.log(telefone); // undefined

// NaN → resultado inválido de operação numérica
let resultado = "abc" * 2;
console.log(resultado); // NaN
